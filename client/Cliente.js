Template.Cliente.events({
    "change #regionesP": function (e) {
        if (jQuery("#regionesP").val() == 'sin-region') {
            swal("error", "seleccione region particular", "error");
            $("#regionesP").val("sin-region");
            $("#comunasP").empty();
        } else {
            setearComunasSegunRegion2();
        }
    },
    "change #comunasP": function (e) {
        if (jQuery("#comunasP").val() == 'sin-region') {
            swal("error", "seleccione region particular", "error");
        } else if (jQuery("#comunasP").val() == 'sin-comuna') {
            swal("error", "seleccione comuna particular", "error");
        }
    },
    "change #regionesE": function (e) {
        if (jQuery("#regionesE").val() == 'sin-region') {
            swal("error", "seleccione region de envio", "error");
            $("#regionesE").val("sin-region");
            $("#comunasE").empty();

        } else {
            setearComunasSegunRegion1();
        }
    },
    "change #comunasE": function (e) {
        console.log("cambiando comunas e");
        if (jQuery("#comunasE").val() == 'sin-region') {
            swal("error", "seleccione region de envio", "error");
        } else if (jQuery("#comunasE").val() == 'sin-comuna') {
            swal("error", "seleccione comuna de envio", "error");
        }
    },

    "click #btnG": function (e) {

        let doc = {};
        doc.nombre = $("#nombre").val();
        doc.apellido = $("#apellido").val();
        doc.email = $("#email").val();
        doc.calleP = $("#calleP").val();
        doc.numeroP = $("#numeroP").val();
        doc.regionesP = $("#regionesP").val();
        doc.comunasP = $("#comunasP").val();
        doc.calleE = $("#calleE").val();
        doc.numeroE = $("#numeroE").val();
        doc.regionesE = $("#regionesE").val();
        doc.comunasE = $("#comunasE").val();
        let client = Session.get("ClienteSeleccionado");
        if (doc.nombre == "" || doc.apellido == "" || doc.email == "" || doc.calleE == "" || doc.calleP == "" || doc.numeroP == "" || doc.regionesP == "" || doc.comunasP == "" || doc.numeroE == "" || doc.regionesE == "" || doc.comunasE == "") {
            swal("error", "favor completar todos los campos", "error");


        } else {
            if (parseInt(doc.numeroE) < 0) {
                swal("error", "el numero de envio debe ser mayor a 0", "error");
            } else {
                if (parseInt(doc.numeroP) < 0) {
                    swal("error", "el numero de particular debe ser mayor a 0", "error");

                } else {
                    if (doc.regionesE == 'sin-region' || doc.regionesP == 'sin-region' || doc.comunasE == 'sin-comuna' || doc.comunasP == 'sin-comuna') {

                        swal("error", "favor confirmar regiones y comunas", "error");
                    } else {
                        if (!ValidateEmail(doc.email)) {

                            swal("error", "favir verificar el email", "error");

                        } else {

                            Meteor.call("AddClients", client ? client._id : false, doc, function (err, resp) {
                                if (!err) {
                                    if (!client._id) {
                                        client._id = resp;
                                    }
                                } else console.warn(err);
                            });
                            $("#modal-ingreso-cliente").hide();
                            limpiarCamposModalCLiente();

                        }

                    }

                }

            }


        }

    },
    "click #addC": function (e) {
        limpiarCamposModalCLiente();
        Session.set("ClienteSeleccionado", {});
        $("#modal-ingreso-cliente").show();
    },
    "click #btnC": function (e) {
        Session.set("ClienteSeleccionado", {});
        limpiarCamposModalCLiente();
        $("#modal-ingreso-cliente").hide();
    },
    "click #btnCa": function (e) {

        limpiarCamposModalCLiente();


    },

    "click .btn-client-delete": function (e) {
        console.log("id: " + e.currentTarget.id);
        Meteor.call("RemoveClients", e.currentTarget.id, function (err, resp) {
            if (!err) {
                console.log("Cliente aniquilado " + resp);
            }
        });
    },
    "click .btn-client-update": function (e) {
        $("#modal-ingreso-cliente").show();
        let id = e.currentTarget.id;
        let doc = ClientList.findOne({ "_id": id });
        Session.set("ClienteSeleccionado", doc);
        $("#regionesP").val(doc.regionesP);
        $("#regionesE").val(doc.regionesE);
        setearComunasSegunRegion1();
        setearComunasSegunRegion2();
        $("#comunasP").val(doc.comunasP);
        $("#comunasE").val(doc.comunasE);
        window.scrollTo(0, 0);
    }

});

